package orgexample_spring_demo1;

public class CD {
	private int id;
	private String name;
	public CD() {
		super();
	}
	public CD(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}